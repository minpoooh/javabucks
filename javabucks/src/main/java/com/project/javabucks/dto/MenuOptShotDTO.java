package com.project.javabucks.dto;

public class MenuOptShotDTO {
	
	private int shotNum;
	private String menuoptCode;
	private String shotType;
	private int shotPrice;
	
	public int getShotNum() {
		return shotNum;
	}
	public void setShotNum(int shotNum) {
		this.shotNum = shotNum;
	}
	public String getMenuoptCode() {
		return menuoptCode;
	}
	public void setMenuoptCode(String menuoptCode) {
		this.menuoptCode = menuoptCode;
	}
	public String getShotType() {
		return shotType;
	}
	public void setShotType(String shotType) {
		this.shotType = shotType;
	}
	public int getShotPrice() {
		return shotPrice;
	}
	public void setShotPrice(int shotPrice) {
		this.shotPrice = shotPrice;
	}	
}
