package com.project.javabucksStore.dto;

public class MenuOptCupDTO {
	
	private int cupNum;
	private String menuoptCode;
	private String cupType;
	private int cupPrice;
	
	public int getCupNum() {
		return cupNum;
	}
	public void setCupNum(int cupNum) {
		this.cupNum = cupNum;
	}
	public String getMenuoptCode() {
		return menuoptCode;
	}
	public void setMenuoptCode(String menuoptCode) {
		this.menuoptCode = menuoptCode;
	}
	public String getCupType() {
		return cupType;
	}
	public void setCupType(String cupType) {
		this.cupType = cupType;
	}
	public int getCupPrice() {
		return cupPrice;
	}
	public void setCupPrice(int cupPrice) {
		this.cupPrice = cupPrice;
	}
	
}
